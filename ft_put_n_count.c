#include "libftprintf.h"

int	ft_put_n_count(int i)
{
	int	len;

	len = 1;
	ft_putnbr_fd(i, 1);
	if (i < 0)
	{
		len++;
		i = -i;
	}
	while (i > 10)
	{
		i = i / 10;
		len++;
	}
	return (len);
}